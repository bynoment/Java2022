public class Main {
    public static void main(String[] args) {
        for(int i=1;i<=10;i+=1){
            System.out.println(i);

        }
        System.out.println("For Döngü bitti");

        //while döngüsü
        int i=1;
        while(i<10){
            System.out.println(i);
            i++;

        }
        System.out.println("While döngüsü bitti");
    }
}